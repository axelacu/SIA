var map = L.mapbox.map('map', 'mapbox.streets')
//.setView([37.5, -120], 7);
var listings = document.getElementById('listings');
var locations = L.mapbox.featureLayer().addTo(map);
locations.setGeoJSON(geoJson);

function setActive(el) {
    var siblings = listings.getElementsByTagName('div');
    for (var i = 0; i < siblings.length; i++) {
        siblings[i].className = siblings[i].className
            .replace(/active/, '').replace(/\s\s*$/, '');
    }
    el.className += ' active';
}


locations.eachLayer(function(locale) {
    var prop = locale.feature.properties;
    var popup = '<h3>Nos activités</h3><div>' + prop.title;
    var listing = listings.appendChild(document.createElement('div'));
    listing.className = 'item';
    var link = listing.appendChild(document.createElement('a'));
    link.href = '#';
    link.className = 'title';
    link.innerHTML = prop.title;
    if (prop.location) {
        link.innerHTML += '<br /><small class="quiet">' + prop.location + '</small>';
        popup += '<br /><small class="quiet">' + prop.location + '</small>';
    }
    var map2 = L.mapbox.tileLayer('mapbox.emerald');
    var details = listing.appendChild(document.createElement('div'));
    if (prop.crop) {
        details.innerHTML += ' Type: ' + prop.crop;
        popup += '<br />Type: ' + prop.crop;
    }
    link.onclick = function() {
        setActive(listing);
        map.setView(locale.getLatLng(), 16);
        map.addLayer(map2);
        locale.openPopup();
        locale.setIcon(L.mapbox.marker.icon({
            'marker-color': '#ff8888',
            'marker-size': 'large'
        }));
        return false;
    };
    locale.on('click', function(e) {
        map.panTo(locale.getLatLng());
        setActive(listing);
        locale.setIcon(L.mapbox.marker.icon({
            'marker-color': '#ff8888',
            'marker-size': 'large'
        }));
    });
    map.on('click', function(e) {
        locale.setIcon(L.mapbox.marker.icon({
            'marker-color': '#00FF00',
            'marker-size': 'large'
        }));
    });
    popup += '</div>';
    locale.bindPopup(popup);
});

